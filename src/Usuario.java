public class Usuario {
    private String nome;
    private String ra;
    private String senha;

    public Usuario() {
        
    }

    public String getRa() {
        return ra;
    }

    public void setRa(String ra) {
        this.ra = ra;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    public Usuario(String nome, String ra, String login) {
        this.nome = nome;
        this.ra = ra;
        this.senha = login;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
    
}
